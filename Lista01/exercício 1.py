#Faça um algoritmo que leia um número inteiro e escreva seu antecessor e sucessor
print('Descubra o antecessor e sucessor de um número inteiro inserindo ele abaixo.')

numeroInteiro = int(input('Insira um número inteiro: '))

antecessor = numeroInteiro - 1
sucessor = numeroInteiro + 1

print('O antecessor do número inteiro inserido é igual a:', antecessor)
print('O sucessor do número inteiro inserido é igual a:', sucessor)


#Faça um algoritmo que leia o ano de nascimento de uma pessoa e
# calcule a idade que completará em 2022.
print('Para calcular quantos anos você completou em 2022, insira a seguinte informação.')

anoNascimento = int(input('Qual o seu ano de nascimento? '))
anoDesejado = 2022

idade = anoDesejado - anoNascimento

print('Em 2022, sua idade era de:', idade, 'anos.')
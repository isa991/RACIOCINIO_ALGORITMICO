'''
4. Leia dois valores reais do teclado, calcular e imprimir na tela:
a) A soma destes valores b) O produto deles c) O quociente entre eles
'''

n1 = float(input('Insira o primeiro valor real: '))
n2 = float(input('Insira o segundo valor real: '))

soma = n1 + n2
print('A soma destes valores é igual a: ', soma)

produto = n1 * n2
print('O produto destes dois valores é igual a: ', produto)

quociente = n1 / n2
print('O quociente destes dois valores é igual a: ', quociente)